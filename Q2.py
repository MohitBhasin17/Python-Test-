def reverse_string(s):
     # This function takes a string 's' and returns its reverse
     # Slicing the string in reverse order
    return s[::-1]

# Defining the original string to be reversed
original_string = "RajasthanOP"

# Calling the reverse_string function and storing the result
reversed_string = reverse_string(original_string)

# Printing the original string
print(f"Original string: {original_string}")
# Printing the reversed string
print(f"Reversed string: {reversed_string}")


#Output
#Original string: RajasthanOP
#Reversed string: POnahtsajaR
